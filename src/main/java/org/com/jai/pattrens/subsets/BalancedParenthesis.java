package org.com.jai.pattrens.subsets;

public class BalancedParenthesis {

    String str;

    int openCount;

    int closeCount;

    public BalancedParenthesis(String str, int openCount, int closeCount) {

        this.str = str;

        this.closeCount = closeCount;

        this.openCount = openCount;

    }


}
